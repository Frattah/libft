#include "libft.h"

int	ft_isin(char c, char const *set)
{
	int	i;
	int	fnd;

	i = -1;
	fnd = 0;
	while (set[++i] && !fnd)
		if (set[i] == c)
			fnd = 1;
	return (fnd);
}
