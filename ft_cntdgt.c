#include "libft.h"

size_t	ft_cntdgt(int n)
{
	size_t	dgt;

	if (n == 0)
		return (1);
	if (n < 0)
		n = -n;
	dgt = 0;
	while (n > 0)
	{
		dgt++;
		n /= 10;
	}
	return (dgt);
}
