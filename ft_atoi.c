#include "libft.h"

int	ft_atoi(const char *s)
{
	size_t	i;
	int		nb;
	int		meno;

	meno = 1;
	nb = 0;
	i = 0;
	while (s[i] && ((s[i] >= 9 && s[i] <= 13) || s[i] == 32))
		i++;
	if (s[i] == '-' || s[i] == '+')
	{
		if (s[i] == '-')
			meno *= -1;
		i++;
	}
	while (ft_isdigit(s[i]))
		nb = (nb * 10) + s[i++] - '0';
	return (meno * nb);
}
